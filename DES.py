    def generate_key_schedule(self):
        key_schedule = []
        for i in range(16):
            key_schedule.append(self.key[i % 8] << (i % 8))
        return key_schedule

    def encrypt(self, plaintext):
        plaintext = self.permute(plaintext, self.IP)
        left, right = plaintext[:32], plaintext[32:]
        for i in range(16):
            left, right = right, self.encrypt_round(left, right, i)
        ciphertext = self.permute(left + right, self.IP_INV)
        return ciphertext

    def decrypt(self, ciphertext):
        ciphertext = self.permute(ciphertext, self.IP)
        left, right = ciphertext[:32], ciphertext[32:]
        for i in range(16):
            left, right = right, self.decrypt_round(left, right, 15 - i)
        plaintext = self.permute(left + right, self.IP_INV)
        return plaintext

    def permute(self, block, table):
        return [block[i] for i in table]

    def encrypt_round(self, left, right, i):
        right = self.permute(right, self.E)
        right ^= self.key_schedule[i]
        right = self.substitute(right, self.S)
        right = self.permute(right, self.P)
        right ^= left
        return right

    def decrypt_round(self, left, right, i):
        right = self.permute(right, self.E)
        right ^= self.key_schedule[i]
        right = self.substitute(right, self.S_INV)
        right = self.permute(right, self.P_INV)
        right ^= left
        return right
    
    def substitute(self, block, table):
        return [table[i][block[i]] for i in range(8)]
    
    def generate_key(self, key):
        key = self.permute(key, self.PC1)
        left, right = key[:28], key[28:]
        for i in range(16):
            left, right = right, self.shift(left, self.SHIFT[i])
            self.key_schedule.append(self.permute(left + right, self.PC2))
    
    def shift(self, block, n):
        return block[n:] + block[:n]

